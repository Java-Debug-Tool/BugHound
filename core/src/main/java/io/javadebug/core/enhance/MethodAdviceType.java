package io.javadebug.core.enhance;

public enum MethodAdviceType {

    ON_RETURN,          // 当方法正常返回的时候
    ON_THROW,           // 当方法抛出异常的时候
    ON_MATCH_PARAM,     // 当方法需要匹配特定参数的时候，type=custom or type=watch的时候
    FULL_MATCH          // 默认情况，相当于 ON_RETURN + ON_THROW

}
