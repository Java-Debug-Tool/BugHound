package io.javadebug.core.security;

import io.javadebug.core.Constant;
import io.javadebug.core.log.PSLogger;
import io.javadebug.core.utils.Strings;

public class ThreadSafeKeeper {

    /**
     *  check whether the caller is at java-debug-tool's thread, if not, this
     *  method will return false to tell the caller, the caller receive
     *  an false after calling this method must break the routine
     *
     * @return true means this ok.
     */
    public static boolean isSafe() {
        Thread caller = Thread.currentThread();
        String callerThreadName = caller.getName();
        if (Strings.isNullOrEmpty(callerThreadName)) {
            PSLogger.error("the call's name is unknown, you must do not continue your work.");
            return false;
        }

        if (!callerThreadName.startsWith(Constant.JAVA_DEBUG_WORKER_NAME_PREFIX)) {
            PSLogger.error(String.format("the caller [%s] attempt to call the 'unsafe code', you should must return.", callerThreadName));
            return false;
        }

        // safe env
        return true;
    }

}
