package io.javadebug.core.security;

import io.javadebug.core.log.PSLogger;
import io.javadebug.core.utils.UTILS;

import java.util.zip.CRC32;

public class CRC32Utils {

    private static final ThreadLocal<CRC32> CRC_32_TL = new ThreadLocal<CRC32>() {
        @Override
        protected CRC32 initialValue() {
            return new CRC32();
        }
    };

    /**
     *  calculate the byte array's crc32 val
     *
     * @param data the data ready to calculate crc32
     * @return the crc32 val, include 0 / -1
     *          0  => the input byte array is null or empty;
     *          -1 => the crc32 calculate fail;
     */
    public static int safeCalculateCRC32(byte[] data) {
        try {
            return calculateCRC32(data);
        } catch (Exception e) {
            PSLogger.error("could not calculate CRC32 cause:" + UTILS.getErrorMsg(e));
            return -1;
        }
    }

    private static int calculateCRC32(byte[] data) {
        if (data == null || data.length == 0) {
            return 0;
        }

        // get the crc instance
        CRC32 crc32 = CRC_32_TL.get();

        // cal
        crc32.update(data);

        int crcVal = (int) crc32.getValue();

        // reset
        crc32.reset();

        // return
        return crcVal;
    }

}
